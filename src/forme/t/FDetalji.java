    public void srediFormu() throws ClassNotFoundException, SQLException, IOException {

        Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
        this.setLocation(dim.width / 2 - this.getSize().width / 2, dim.height / 2 - this.getSize().height / 2);

        listaDesno = Kontroler.vratiInstancu().vratiSvePreparateTretmana(tretmanID);
        DefaultListModel dlm = new DefaultListModel();
        for (int i = 0; i < listaDesno.size(); i++) {
            dlm.addElement(listaDesno.get(i));
        }
        jListIzabraniPreparati.setModel(dlm);
        listaLevo = new ArrayList<>(Kontroler.vratiInstancu().vratiSvePreparate());

        for (int i = 0; i < listaLevo.size(); i++) {
            for (int j = 0; j < listaDesno.size(); j++) {
                if (listaLevo.get(i).equals(listaDesno.get(j))) {
                    listaLevo.remove(i);
                }
            }
        }

        PModelTabele pmt = new PModelTabele(listaLevo);
        jtblPreparati.setModel(pmt);

    }

