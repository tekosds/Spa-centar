    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed

        String opis = jtxtOpis.getText().trim();
        String trajanje = jtxtTrajanje.getText().trim();
        double cena = 0;
        boolean validacija = true;

        if (opis == null || opis.equals("") || trajanje == null || trajanje.equals("")) {
            validacija = false;
        }

        if (validacija == true) {
            Tretman t = new Tretman();
            t.setOpis(opis);
            t.setTrajanjeUMin(Integer.parseInt(trajanje));

            lp = new ArrayList<Preparat>();

            ListModel model = jListIzabraniPreparati.getModel();
            for (int i = 0; i < model.getSize(); i++) {
                Preparat p = (Preparat) model.getElementAt(i);
                lp.add(p);
                cena += p.getCena();
            }
            t.setCena(cena);
            t.setListaPreparata(lp);
            try {
                Kontroler.vratiInstancu().unesiTretman(t);
            } catch (SQLException ex) {
                Logger.getLogger(FTretman.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FTretman.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(FTretman.class.getName()).log(Level.SEVERE, null, ex);
            }

            try {
                resetujFormu();
                jtxtPoruka.setVisible(true);
                jtxtPoruka.setText("Uspesno ste uneli tretman!");

            } catch (Exception e) {
                jtxtPoruka.setText(e.getMessage());
            }
        } else {
            jtxtPoruka.setText("Morate popuniti sva polja!");

        }


    }//GEN-LAST:event_jbtnSacuvajActionPerformed

