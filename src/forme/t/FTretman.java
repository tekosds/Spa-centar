    private void jbtnObrisiPreparatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnObrisiPreparatActionPerformed
        listaLevo.add(listaDesno.get(jListIzabraniPreparati.getSelectedIndex()));
        listaDesno.remove(jListIzabraniPreparati.getSelectedIndex());
        pmt = new PModelTabele(listaLevo);
        jtblPreparati.setModel(pmt);
        dlm = new DefaultListModel<Preparat>();
        for (Preparat p : listaDesno) {
            dlm.addElement(p);
        }
        jListIzabraniPreparati.setModel(dlm);
    }//GEN-LAST:event_jbtnObrisiPreparatActionPerformed

