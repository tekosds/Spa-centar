    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed

        String imePrezime = jtxtImePrezime.getText().trim();
        String stepenSS = (String) comboSS.getSelectedItem();
        String sDatum = jtxtDatumRodjenja.getText().trim();
       
        boolean validacija = true;
        Date datum = null;
        if (!sDatum.isEmpty()) {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            try {
                datum = sdf.parse(sDatum);
            } catch (ParseException ex) {
                validacija = false;
                errMaticniBroj.setText("Datum mora biti u formatu: dd/MM/yyyy. ");
            }
        }
        if (imePrezime.isEmpty()) {
            errImePrezime.setText("Morate uneti ime i prezime. ");
            validacija = false;
        }              
                                     
         if (stepenSS==null) {
            errStepenObrazovanja.setText("Morate izabrati stepen strucne spreme ");
            validacija = false;
        }
         
                  

        if (validacija == true) {
            Zaposleni z = new Zaposleni();
            z.setImePrezime(imePrezime);
            z.setStepenSS(stepenSS);
            z.setDatumRodjenja(datum);
            
            resetujLabele();
            try {
                Kontroler.vratiInstancu().unesiZaposlenog(z);
                jtxtPoruka.setVisible(true);
                jtxtPoruka.setText("Zaposleni je uspesno sacuvan");
            } catch (Exception ex) {
               jtxtPoruka.setText(ex.getMessage());
            }

        }
    }//GEN-LAST:event_jbtnSacuvajActionPerformed

